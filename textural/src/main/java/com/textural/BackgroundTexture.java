package com.textural;

public class BackgroundTexture {

    private int[] listColorCodes;
    private int numberLines;
    private int numberColumns;

    public int[] getListColorCodes() {
        return listColorCodes;
    }

    public void setListColorCodes(int[] listColorCodes) {
        this.listColorCodes = listColorCodes;
    }

    public int getNumberLines() {
        return numberLines;
    }

    public void setNumberLines(int numberLines) {
        this.numberLines = numberLines;
    }

    public int getNumberColumns() {
        return numberColumns;
    }

    public void setNumberColumns(int numberColumns) {
        this.numberColumns = numberColumns;
    }

}
